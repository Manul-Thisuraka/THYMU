public class MedicineInfo {


        private String nameMedi;
        private String reasonMedi;
        private String formMedi;
        private String timeMedi;

        public String getNameMedi() {
            return nameMedi;
        }

        public String getReasonMedi() {
            return reasonMedi;
        }


        public String getTimeMedi() {
            return timeMedi;
        }

        public MedicineInfo() {

        }

        public void setNameMedi(String nameMedi) {
            this.nameMedi = nameMedi;
        }

        public void setReasonMedi(String reasonMedi) {
            this.reasonMedi = reasonMedi;
        }

        public void setFormMedi(String formMedi) {
            this.formMedi = formMedi;
        }

        public void setTimeMedi(String timeMedi) {
            this.timeMedi = timeMedi;
        }

        public String getFormMedi() {
            return formMedi;
        }

}

