package com.example.thymu;

public class waterDrink {

    private String drinkDate;
    private String drinkAmount;
    private String totalDrink;

    public  waterDrink(){

    }

    public String getDrinkDate(){

        return drinkDate;
    }

    public  void setDrinkDate(String drinkDate){

        this.drinkDate = drinkDate;
    }

    public String getDrinkAmount(){

        return drinkAmount;
    }

    public  void setDrinkAmount(String drinkAmount){

        this.drinkAmount = drinkAmount;
    }

    public String getTotalDrink(){

        return totalDrink;
    }

    public  void setTotalDrink(String totalDrink){

        this.totalDrink = totalDrink;
    }
}
